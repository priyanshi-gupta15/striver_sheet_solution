package step1.lec1;
//while loop
public class que7 {
  public static void main(String[] args) {
    int count = 0;
    while (count < 5) {
      System.out.println("Count is: " + count);
      count++;

    }
    // Do-while loop
    count = 0;
    do {
      System.out.println("Count in do-while is: " + count);
      count++;
    } while (count < 5);


  }
}
